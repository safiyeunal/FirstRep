package com.syntax.class01;

public class PrintlnVsPrint {

	public static void main(String[] args) {
		
		// println --> prints and creates new line
		// print --> prints and no new line is created
		
		System.out.println("I am 27 years old");

		System.out.println("I am very happy");
		
		System.out.print("I am learning Java");
		
		System.out.print(" and I am excited");
	}

}
